package com.TypeConversion;

// JAVA program for convert long to int

public class LongtoInt {
	public static void main(String[] args) {
		
		// long intu
		long l1 = 232352342L;
		long l2 = 5345436L;
		
		// convert long into int 
		// using typecasting
		
		int no1 = (int)l1;
		int no2 = (int)l2;
		
		// print output
		System.out.println("long to int value is : " + no1);
		System.out.println("long to int value is : " + no2);
	}
}
